// Data input
let a = "10";
let b = "20";

// Type conversion from String to Number
a = Number(a);
b= Number(b);
 
// Arithmetic Operations
console.log("Addition of a & b: ", a+b);
console.log("Subtraction of a & b: ", a-b);
console.log("Multiplication of a & b: ", a*b);
console.log("Division of a & b: ", a/b);
console.log("Modulus of a & b: ", a%b);

// Strictly comparison operator
console.log("Equality of a & b: ", a==b);
console.log("Not Equality of a & b: ", a!=b);
console.log("Strictly Equality of a & b: ", a===b);
console.log("Strictly Not Equality of a & b: ", a!==b);